'''

Write the necessary code to display the area and perimeter of a rectangle that has a width of 2.4 and a height of 6.4.

'''

# Area of a rectangle with width = 2.4 and height = 6.4. Area = width * height = 2.4 * 6.4
print (2.4 * 6.4)

# Perimter  of a rectangle with width = 2.4 and height = 6.4. Perimeter = 2* width + 2 * height = (2 * 2.4) + (2 * 6.4)
print ((2 * 2.4) + (2* 6.4))