'''
Write the necessary code calculate the volume and surface area
of a cylinder with a radius of 3.14 and a height of 5. Print out the result.


'''

#  cylinder radius (r) = 3.14 , height (h) = 5.
# volume (V) of a cylinder is V = π * (radius ** 2) * height
# π = 3.14
print(3.14 * (3.14 **2) * 5) # Volume of the cylinder

# surface area (A) of a cylinder is A = 2 * π * r * h + 2 * π * (r ** 2)
# π = 3.14
print(2 * 3.14 * 3.14 * 5 + 2 * 3.14 * (3.14 ** 2)) # is the surface area (A) of the cylinder